public class HelloJava {
    /*
        class: 실행 파일. 파일명과 class명은 일치해야한다.
        {} : class의 범위를 지정하는 블록을 표시
        '    ': 코드 구분을 위해 관례상 space 4칸으로 구분한다. 주로 tab으로 사용(다른 설정의 IDE를 사용하는 경우 주의)
     */
    public static void main(String[] args){
        /*
            method_name(parameter){code block}: method를 표시하는 방식
            System.out.println(): console에 표기하는 method를 외부에서 호출
            ;: 코드 한 줄이 종료됨을 표시
        */
        System.out.println("Hello JAVA!!");
        System.out.println("new String");
        System.out.println("new new");
        //순차적으로 진행
    }
}
